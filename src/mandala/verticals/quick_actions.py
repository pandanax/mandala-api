def expand_inbound_quick_action(vertical_id: str, text: str | None) -> str | None:
    """Если ``text`` — известный код кнопки, подставить полный запрос; иначе без изменений."""
    if text is None:
        return None
    raw = text.strip()
    if not raw:
        return text
    v = vertical_id.strip()
    table = _ASTROLOGY if v == "astrology" else _THERAPY if v == "therapy" else {}
    expanded = table.get(raw)
    if expanded is not None:
        return expanded
    return text
